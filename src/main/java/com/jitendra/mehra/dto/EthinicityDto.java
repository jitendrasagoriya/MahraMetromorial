package com.jitendra.mehra.dto;

public class EthinicityDto {
	private String religion;
	private String motherToung;
	private String ethinicity ;
	private String gotra;
	private String caste;
	private String gotraToSave;
	/**
	 * @return the religion
	 */
	public String getReligion() {
		return religion;
	}
	/**
	 * @param religion the religion to set
	 */
	public void setReligion(String religion) {
		this.religion = religion;
	}
	/**
	 * @return the motherToung
	 */
	public String getMotherToung() {
		return motherToung;
	}
	/**
	 * @param motherToung the motherToung to set
	 */
	public void setMotherToung(String motherToung) {
		this.motherToung = motherToung;
	}
	/**
	 * @return the ethinicity
	 */
	public String getEthinicity() {
		return ethinicity;
	}
	/**
	 * @param ethinicity the ethinicity to set
	 */
	public void setEthinicity(String ethinicity) {
		this.ethinicity = ethinicity;
	}
	/**
	 * @return the gotra
	 */
	public String getGotra() {
		return gotra;
	}
	/**
	 * @param gotra the gotra to set
	 */
	public void setGotra(String gotra) {
		this.gotra = gotra;
	}
	/**
	 * @return the caste
	 */
	public String getCaste() {
		return caste;
	}
	/**
	 * @param caste the caste to set
	 */
	public void setCaste(String caste) {
		this.caste = caste;
	}
	/**
	 * @return the gotraToSave
	 */
	public String getGotraToSave() {
		return gotraToSave;
	}
	/**
	 * @param gotraToSave the gotraToSave to set
	 */
	public void setGotraToSave(String gotraToSave) {
		this.gotraToSave = gotraToSave;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "EthinicityDto [religion=" + religion + ", motherToung=" + motherToung + ", ethinicity=" + ethinicity
				+ ", gotra=" + gotra + ", caste=" + caste + ", gotraToSave=" + gotraToSave + "]";
	}
	
	
	
}
